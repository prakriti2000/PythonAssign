is_num = lambda q: q.replace('.','',1).isdigit()
print(is_num('1234'))
print(is_num('3.426'))
print(is_num('-1234'))
print(is_num('00'))
print(is_num('ab123'))
print(is_num('00111'))
print("\nPrint checking numbers:")
is_num1 = lambda r: is_num(r[1:]) if r[0]=='-' else is_num(r)
print(is_num1('-15.2'))
print(is_num1('ab12'))